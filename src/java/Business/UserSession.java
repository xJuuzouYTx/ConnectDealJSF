package Business;

/**
 *
 * @author JuuzouYT
 */
public class UserSession {
    
    String name;
    String lastname;
    String email;
    String code;
    String password;
    String salt;

    public UserSession() {
    }

    public UserSession(String name, String lastname, String email, String password, String code) {
        this.name = name;
        this.lastname = lastname;
        this.email = email;
        this.code = code;
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
